
<h3>C'est quoi GESFOR?</h3><br>

GESFOR est une application web conçue pour la gestion du forage de Séokhaye dont la
gestion respecte le standard défini par le réseau ASUFOR.<br/>

<h3>POURQUOI GESFOR ?</h3><br/>
Le standard défini par ASUFOR pour la gestion des forages a pour but :<br/>
->La mise en place d’un système de management innovant au sein des structures de gestion de forage<br/>
->La modernisation du système de gestion des forages<br/>
->La facilité de maintenance des forages<br/>
->La qualité dans la production et la distribution de l’eau potable<br/>

Cependant, à l’image de Séokhaye, la quasi-totalité des forages qui ont adopté ce standard ne
dispose d’aucun système informatique moderne capable d’assister les gérants dans leurs
tâches. Cela présente un handicap majeur pour une gestion conforme au standard et constitue
un frein dans le développement. En effet on note :<br/>

->Trop de dépense pour la main d’œuvre<br/>
->Une lenteur considérable dans le traitement des services (facturation, indexation...)<br/>
->Un risque permanent de perte d’archives et de données importantes....<br/>
->Des difficultés liées au contrôle de la gestion et du personnel.<br/>
C'est dans ce contexte que GESFOR a été développé afin de répondre aux besoins qu’exige
une gestion informatique moderne et dynamique.<br/>

<h3>COMMENT FONCTIONNE -T-ELLE ?</h3><br/>
GESFOR est une application web. Elle fonctionne sous apache et a été développée
principalement en php. D’autres langages, des Framework et des bibliothèques sont aussi
utilisés dans la conception de l’application.
L’application peut fonctionner :<br/>
->En mono poste : sur un seul ordinateur<br/>
->En réseau local : plusieurs ordinateurs reliés en réseau (une entreprise par exemple)<br/>
->Via Internet : un accès à distance avec une connexion internet partout dans le monde.<br/>
